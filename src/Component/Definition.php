<?php

namespace Kanel\Enuma\Component;

abstract class Definition
{
    protected $comment = '';

    /**
     * @return string
     */
    public function getComment(): string
    {
        return $this->comment;
    }

    /**
     * @param string $comment
     */
    public function setComment(string $comment)
    {
        $this->comment = $comment;
    }
}